#include "libft.h"

char	*ft_strdup(const char *s1)
{
	char	*str;
	int		l;
	int		i;

	l = ft_strlen(s1);
	if (!(str = (char*)malloc(sizeof(char) * (l + 1))))
		return (NULL);
	i = -1;
	while (s1[++i])
		str[i] = s1[i];
	str[i] = '\0';
	return (str);
}
