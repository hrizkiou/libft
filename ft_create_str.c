#include "libft.h"

char	*ft_create_str(int len, int n, int neg, char *str)
{
	while (len >= 0)
	{
		str[len] = (n % 10) + 48;
		n = n / 10;
		len--;
	}
	if (neg < 0)
		str[0] = '-';
	return (str);
}
