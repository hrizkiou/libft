#include "libft.h"

size_t	ft_cnbr(int n)
{
	size_t count;

	count = 1;
	if (n < 0)
	{
		count++;
		n = -n;
	}
	while (n > 9)
	{
		n = n / 10;
		count++;
	}
	return (count);
}
