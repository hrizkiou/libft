#include "libft.h"

void	ft_putnbr(int n)
{
	unsigned	int	nbr;

	nbr = n;
	if (n < 0)
	{
		nbr = -n;
		ft_putchar('-');
	}
	if (nbr > 9)
	{
		ft_putnbr(nbr / 10);
		ft_putnbr(nbr % 10);
	}
	else
		ft_putchar(nbr + '0');
}
